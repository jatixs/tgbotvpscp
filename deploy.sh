#!/bin/bash

BOT_INSTALL_PATH="/opt/tg-bot"
SERVICE_NAME="tg-bot"
SERVICE_USER="tgbot"
PYTHON_BIN="/usr/bin/python3"
VENV_PATH="${BOT_INSTALL_PATH}/venv"

RELEASE_BOT_PY_URL="https://raw.githubusercontent.com/jatixs/tgbotvpscp/main/bot.py"
RELEASE_REQUIREMENTS_URL="https://raw.githubusercontent.com/jatixs/tgbotvpscp/main/requirements.txt"

FEATURE_BOT_PY_URL="https://raw.githubusercontent.com/jatixs/tgbotvpscp/feature/bot.py"
FEATURE_REQUIREMENTS_URL="https://raw.githubusercontent.com/jatixs/tgbotvpscp/feature/requirements.txt"

C_RESET='\033[0m'
C_RED='\033[0;31m'
C_GREEN='\033[0;32m'
C_YELLOW='\033[0;33m'
C_BLUE='\033[0;34m'
C_CYAN='\033[0;36m'
C_BOLD='\033[1m'

msg_info() { echo -e "${C_CYAN}üîµ $1${C_RESET}"; }
msg_success() { echo -e "${C_GREEN}‚úÖ $1${C_RESET}"; }
msg_error() { echo -e "${C_RED}‚ùå $1${C_RESET}"; }
msg_question() { read -p "$(echo -e "${C_YELLOW}‚ùì $1${C_RESET}")" $2; }

spinner() {
    local pid=$1
    local msg=$2
    local spin='|/-\'
    local i=0
    while kill -0 $pid 2>/dev/null; do
        i=$(( (i+1) %4 ))
        printf "\r${C_BLUE}‚è≥ ${spin:$i:1} ${msg}...${C_RESET}"
        sleep .1
    done
    printf "\r"
}

run_with_spinner() {
    local msg=$1
    shift
    "$@" > /dev/null 2>&1 &
    local pid=$!
    spinner $pid "$msg"
    wait $pid
    return $?
}

if command -v curl &> /dev/null; then
    DOWNLOADER="curl -sSLf"
elif command -v wget &> /dev/null; then
    DOWNLOADER="wget -qO-"
else
    msg_error "–ù–∏ curl, –Ω–∏ wget –Ω–µ –Ω–∞–π–¥–µ–Ω—ã. –£—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ –æ–¥–∏–Ω –∏–∑ –Ω–∏—Ö."
    exit 1
fi

install_extras() {
    if ! command -v fail2ban-client &> /dev/null; then
        msg_question "Fail2Ban –Ω–µ –Ω–∞–π–¥–µ–Ω. –•–æ—Ç–∏—Ç–µ —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –µ–≥–æ? (y/n): " INSTALL_F2B
        if [[ "$INSTALL_F2B" =~ ^[Yy]$ ]]; then
            run_with_spinner "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ Fail2Ban" sudo apt install -y fail2ban
            sudo systemctl enable fail2ban &> /dev/null
            sudo systemctl start fail2ban &> /dev/null
            msg_success "Fail2Ban —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –∏ –∑–∞–ø—É—â–µ–Ω."
        fi
    fi
    if ! command -v speedtest &> /dev/null; then
        msg_question "Speedtest-CLI –Ω–µ –Ω–∞–π–¥–µ–Ω. –•–æ—Ç–∏—Ç–µ —É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –µ–≥–æ? (y/n): " INSTALL_SPEEDTEST
        if [[ "$INSTALL_SPEEDTEST" =~ ^[Yy]$ ]]; then
            run_with_spinner "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ Speedtest-CLI" sudo apt install -y speedtest-cli
            msg_success "Speedtest-CLI —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω."
        fi
    fi
}

common_install_steps() {
    local branch=$1
    if [ "$branch" == "release" ]; then
        BOT_PY_URL=$RELEASE_BOT_PY_URL
        REQUIREMENTS_URL=$RELEASE_REQUIREMENTS_URL
    else
        BOT_PY_URL=$FEATURE_BOT_PY_URL
        REQUIREMENTS_URL=$FEATURE_REQUIREMENTS_URL
    fi

    msg_info "1. –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –±–∞–∑–æ–≤—ã—Ö –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π..."
    run_with_spinner "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ —Å–ø–∏—Å–∫–∞ –ø–∞–∫–µ—Ç–æ–≤" sudo apt update -y
    run_with_spinner "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π (python, git, curl...)" sudo apt install -y python3 python3-pip python3-venv git curl wget sudo
    
    install_extras
    sudo mkdir -p ${BOT_INSTALL_PATH}
    
    msg_info "2. –°–∫–∞—á–∏–≤–∞–Ω–∏–µ —Ñ–∞–π–ª–æ–≤ –ø—Ä–æ–µ–∫—Ç–∞ –∏–∑ –≤–µ—Ç–∫–∏ '${branch}'..."
    if ! ${DOWNLOADER} "${BOT_PY_URL}" | sudo tee "${BOT_INSTALL_PATH}/bot.py" > /dev/null; then
        msg_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å bot.py."
        exit 1
    fi
    if ! ${DOWNLOADER} "${REQUIREMENTS_URL}" | sudo tee "${BOT_INSTALL_PATH}/requirements.txt" > /dev/null; then
        msg_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å requirements.txt."
        exit 1
    fi
    echo "$branch" | sudo tee "${BOT_INSTALL_PATH}/.branch" > /dev/null
}

install_logic() {
    local install_mode=$1
    local branch=$2

    common_install_steps $branch
    
    local exec_user_cmd=""
    if [ "$install_mode" == "secure" ]; then
        msg_info "3. –°–æ–∑–¥–∞–Ω–∏–µ —Å–∏—Å—Ç–µ–º–Ω–æ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è '${SERVICE_USER}'..."
        if ! id "${SERVICE_USER}" &>/dev/null; then
            sudo useradd -r -s /bin/false -d ${BOT_INSTALL_PATH} ${SERVICE_USER}
        fi
        sudo chown -R ${SERVICE_USER}:${SERVICE_USER} ${BOT_INSTALL_PATH}
        sudo chmod -R 750 ${BOT_INSTALL_PATH}
        exec_user_cmd="sudo -u ${SERVICE_USER}"
    else
        sudo chown -R root:root ${BOT_INSTALL_PATH}
        sudo chmod -R 755 ${BOT_INSTALL_PATH}
    fi

    msg_info "4. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –≤–∏—Ä—Ç—É–∞–ª—å–Ω–æ–≥–æ –æ–∫—Ä—É–∂–µ–Ω–∏—è Python..."
    pushd ${BOT_INSTALL_PATH} > /dev/null
    if [ ! -d "${VENV_PATH}" ]; then
        run_with_spinner "–°–æ–∑–¥–∞–Ω–∏–µ venv" $exec_user_cmd ${PYTHON_BIN} -m venv venv
    fi
    run_with_spinner "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ pip" $exec_user_cmd ${VENV_PATH}/bin/pip install --upgrade pip
    run_with_spinner "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π Python" $exec_user_cmd ${VENV_PATH}/bin/pip install -r requirements.txt
    if [ $? -ne 0 ]; then
        msg_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ –∑–∞–≤–∏—Å–∏–º–æ—Å—Ç–µ–π Python."
        popd > /dev/null
        exit 1
    fi

    msg_info "5. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö –æ–∫—Ä—É–∂–µ–Ω–∏—è..."
    msg_question "–í–≤–µ–¥–∏—Ç–µ –≤–∞—à Telegram Bot Token: " TG_BOT_TOKEN_USER
    msg_question "–í–≤–µ–¥–∏—Ç–µ –≤–∞—à Telegram User ID (—Ç–æ–ª—å–∫–æ —Ü–∏—Ñ—Ä—ã): " TG_ADMIN_ID_USER

    sudo tee .env > /dev/null <<EOF
TG_BOT_TOKEN="${TG_BOT_TOKEN_USER}"
TG_ADMIN_ID="${TG_ADMIN_ID_USER}"
INSTALL_MODE="${install_mode}"
EOF

    local owner="root:root"
    if [ "$install_mode" == "secure" ]; then owner="${SERVICE_USER}:${SERVICE_USER}"; fi
    sudo chown ${owner} .env
    sudo chmod 600 .env
    popd > /dev/null
    
    if [ "$install_mode" == "root" ]; then
      msg_info "6. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ –ø—Ä–∞–≤ sudo..."
      SUDOERS_FILE="/etc/sudoers.d/98-${SERVICE_NAME}-root"
      sudo tee ${SUDOERS_FILE} > /dev/null <<EOF
root ALL=(ALL) NOPASSWD: /bin/systemctl restart ${SERVICE_NAME}.service
root ALL=(ALL) NOPASSWD: /sbin/reboot
EOF
      sudo chmod 440 ${SUDOERS_FILE}
    fi

    create_and_start_service $install_mode
}

create_and_start_service() {
    local install_mode=$1
    local user="root"
    local group="root"
    if [ "$install_mode" == "secure" ]; then
        user=${SERVICE_USER}
        group=${SERVICE_USER}
    fi
    
    msg_info "–°–æ–∑–¥–∞–Ω–∏–µ systemd —Å–µ—Ä–≤–∏—Å–∞..."
    SERVICE_FILE="/etc/systemd/system/${SERVICE_NAME}.service"
    sudo tee ${SERVICE_FILE} > /dev/null <<EOF
[Unit]
Description=${SERVICE_NAME} Telegram Bot
After=network.target
[Service]
Restart=always
RestartSec=5
User=${user}
Group=${group}
WorkingDirectory=${BOT_INSTALL_PATH}
EnvironmentFile=${BOT_INSTALL_PATH}/.env
ExecStart=${VENV_PATH}/bin/python ${BOT_INSTALL_PATH}/bot.py
[Install]
WantedBy=multi-user.target
EOF

    msg_info "–ó–∞–ø—É—Å–∫ –∏ –∞–∫—Ç–∏–≤–∞—Ü–∏—è —Å–µ—Ä–≤–∏—Å–∞..."
    sudo systemctl daemon-reload
    sudo systemctl enable ${SERVICE_NAME}.service &> /dev/null
    sudo systemctl start ${SERVICE_NAME}.service
    sleep 2

    if sudo systemctl is-active --quiet ${SERVICE_NAME}.service; then
        msg_success "–°–µ—Ä–≤–∏—Å ${SERVICE_NAME} —É—Å–ø–µ—à–Ω–æ –∑–∞–ø—É—â–µ–Ω!"
        echo -e "\n${C_GREEN}${C_BOLD}üéâ –£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞! –ù–∞–ø–∏—à–∏—Ç–µ –±–æ—Ç—É /start.${C_RESET}\n"
    else
        msg_error "–°–µ—Ä–≤–∏—Å –Ω–µ –∑–∞–ø—É—Å—Ç–∏–ª—Å—è. –õ–æ–≥–∏: sudo journalctl -u ${SERVICE_NAME} -xe"
        exit 1
    fi
}

uninstall_bot() {
    echo -e "\n${C_BOLD}=== –ù–∞—á–∞–ª–æ —É–¥–∞–ª–µ–Ω–∏—è Telegram-–±–æ—Ç–∞ ===${C_RESET}"
    msg_question "–í–´ –£–í–ï–†–ï–ù–´, —á—Ç–æ —Ö–æ—Ç–∏—Ç–µ –ü–û–õ–ù–û–°–¢–¨–Æ —É–¥–∞–ª–∏—Ç—å –±–æ—Ç–∞? (y/n): " confirm
    if [[ ! "$confirm" =~ ^[Yy]$ ]]; then
        msg_info "–£–¥–∞–ª–µ–Ω–∏–µ –æ—Ç–º–µ–Ω–µ–Ω–æ."
        return
    fi

    msg_info "1. –û—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏ –æ—Ç–∫–ª—é—á–µ–Ω–∏–µ —Å–µ—Ä–≤–∏—Å–∞..."
    sudo systemctl stop ${SERVICE_NAME} &> /dev/null
    sudo systemctl disable ${SERVICE_NAME} &> /dev/null

    msg_info "2. –£–¥–∞–ª–µ–Ω–∏–µ —Å–∏—Å—Ç–µ–º–Ω—ã—Ö —Ñ–∞–π–ª–æ–≤..."
    sudo rm -f "/etc/systemd/system/${SERVICE_NAME}.service"
    sudo rm -f "/etc/sudoers.d/98-${SERVICE_NAME}-root"
    sudo systemctl daemon-reload

    msg_info "3. –£–¥–∞–ª–µ–Ω–∏–µ –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏–∏ –∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è..."
    sudo rm -rf "${BOT_INSTALL_PATH}"
    if id "${SERVICE_USER}" &>/dev/null; then
        sudo userdel -r "${SERVICE_USER}"
    fi
    msg_success "–£–¥–∞–ª–µ–Ω–∏–µ –ø–æ–ª–Ω–æ—Å—Ç—å—é –∑–∞–≤–µ—Ä—à–µ–Ω–æ."
}

update_bot() {
    echo -e "\n${C_BOLD}=== –ù–∞—á–∞–ª–æ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è bot.py ===${C_RESET}"
    if [ ! -f "${BOT_INSTALL_PATH}/.branch" ]; then
        msg_error "–ù–µ —É–¥–∞–ª–æ—Å—å –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω–Ω—É—é –≤–µ—Ç–∫—É. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–µ—Ä–µ—É—Å—Ç–∞–Ω–æ–≤–∏—Ç—å –±–æ—Ç–∞."
        exit 1
    fi
    
    local branch=$(sudo cat "${BOT_INSTALL_PATH}/.branch")
    msg_info "–û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –≤–µ—Ç–∫–∏: ${branch}"

    if [ "$branch" == "release" ]; then
        BOT_PY_URL=$RELEASE_BOT_PY_URL
    else
        BOT_PY_URL=$FEATURE_BOT_PY_URL
    fi
    
    msg_info "1. –°–∫–∞—á–∏–≤–∞–Ω–∏–µ –Ω–æ–≤–æ–π –≤–µ—Ä—Å–∏–∏ bot.py..."
    if ${DOWNLOADER} "${BOT_PY_URL}" | sudo tee "${BOT_INSTALL_PATH}/bot.py" > /dev/null; then
        msg_success "–§–∞–π–ª bot.py —É—Å–ø–µ—à–Ω–æ –æ–±–Ω–æ–≤–ª–µ–Ω."
    else
        msg_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å —Ñ–∞–π–ª."
        exit 1
    fi

    msg_info "2. –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫ —Å–µ—Ä–≤–∏—Å–∞..."
    if sudo systemctl restart ${SERVICE_NAME}; then
        msg_success "–°–µ—Ä–≤–∏—Å ${SERVICE_NAME} —É—Å–ø–µ—à–Ω–æ –ø–µ—Ä–µ–∑–∞–ø—É—â–µ–Ω."
        echo -e "\n${C_GREEN}${C_BOLD}üéâ –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –∑–∞–≤–µ—Ä—à–µ–Ω–æ!${C_RESET}\n"
    else
        msg_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–µ. –õ–æ–≥–∏: sudo journalctl -u ${SERVICE_NAME} -xe"
        exit 1
    fi
}

show_install_menu() {
    clear
    echo -e "${C_BOLD}--- –ú–µ–Ω—é –£—Å—Ç–∞–Ω–æ–≤–∫–∏ ---${C_RESET}"
    echo "1) Release (—Å—Ç–∞–±–∏–ª—å–Ω–∞—è –≤–µ—Ä—Å–∏—è)"
    echo "2) Feature (—Ç–µ—Å—Ç–æ–≤–∞—è —Ñ—É–Ω–∫—Ü–∏–æ–Ω–∞–ª—å–Ω–æ—Å—Ç—å)"
    read -p "–í—ã–±–µ—Ä–∏—Ç–µ –≤–µ—Ç–∫—É –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ [1-2]: " branch_choice
    
    local branch="release"
    if [ "$branch_choice" == "2" ]; then
        branch="feature"
    fi

    echo -e "\n${C_BOLD}--- –†–µ–∂–∏–º –£—Å—Ç–∞–Ω–æ–≤–∫–∏ ---${C_RESET}"
    echo "1) Secure (—Ä–µ–∫–æ–º–µ–Ω–¥—É–µ–º—ã–π, –±–µ–∑–æ–ø–∞—Å–Ω—ã–π)"
    echo "2) Root (–ø–æ–ª–Ω—ã–π –¥–æ—Å—Ç—É–ø, –º–µ–Ω–µ–µ –±–µ–∑–æ–ø–∞—Å–Ω—ã–π)"
    read -p "–í—ã–±–µ—Ä–∏—Ç–µ —Ä–µ–∂–∏–º —É—Å—Ç–∞–Ω–æ–≤–∫–∏ [1-2]: " mode_choice

    if [ "$mode_choice" == "1" ]; then
        install_logic "secure" "$branch"
    elif [ "$mode_choice" == "2" ]; then
        if [ "$(id -u)" -ne 0 ]; then
            msg_error "–î–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –≤ —Ä–µ–∂–∏–º–µ Root, –∑–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç —Å 'sudo'."
            exit 1
        fi
        install_logic "root" "$branch"
    else
        msg_error "–ù–µ–≤–µ—Ä–Ω—ã–π –≤—ã–±–æ—Ä."
    fi
}

main_menu() {
    clear
    echo -e "${C_BLUE}${C_BOLD}"
    echo "‚ïî‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïó"
